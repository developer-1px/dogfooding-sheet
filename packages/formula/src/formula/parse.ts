export const FUNC_RE = new RegExp(`(${FUNC_NAMES})\\(([^()]*)\\)`, 'gi')

export const expandRange = (a: string, ar: string, b: string, br: string): string[] => {
  const c1 = a.charCodeAt(0), c2 = b.charCodeAt(0)
  const r1 = Number(ar), r2 = Number(br)
  const colCount = Math.abs(c2 - c1) + 1
  const rowCount = Math.abs(r2 - r1) + 1
  const refCount = colCount * rowCount
  if (
    !Number.isSafeInteger(r1) || !Number.isSafeInteger(r2) ||
    !Number.isSafeInteger(refCount) || refCount > MAX_EXPANDED_REFS
  ) throw new RangeLimitError()
  const refs: string[] = []
  for (let c = Math.min(c1, c2); c <= Math.max(c1, c2); c++) {
    for (let r = Math.min(r1, r2); r <= Math.max(r1, r2); r++) {
      refs.push(String.fromCharCode(c) + r)
    }
  }
  return refs
}

export const collectRefs = (args: string): string[] => {
  const refs: string[] = []
  args.replace(RANGE_RE, (_x, a, ar, b, br) => {
    refs.push(...expandRange(a, ar, b, br))
    return ''
  })
  args.replace(ABS_A1_RE, (_x, _absCol, c, _absRow, r) => {
    const ref = `${c}${r}`
    if (!refs.includes(ref)) refs.push(ref)
    return ''
  })
  return refs
}

export function refsInFormula(raw: string): string[] {
  if (!raw.startsWith('=')) return []
  try {
    return collectRefs(raw.slice(1))
  } catch (error) {
    if (error instanceof RangeLimitError) return []
    throw error
  }
}
